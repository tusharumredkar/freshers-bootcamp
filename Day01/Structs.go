package main

import "fmt"

type person struct {
	name string
	age  int
}

func newPerson(name string) *person {
	p := person{name: name}
	p.age = 42
	return &p
}

func main() {

	fmt.Println(person{"Tushar", 25})

	fmt.Println(person{name: "Nikhil", age: 20})

	fmt.Println(person{name: "Bob"})

	fmt.Println(&person{name: "Nick", age: 40})

	fmt.Println(newPerson("Andrew"))

	s := person{name: "Sean", age: 50}
	fmt.Println(s.name)

	sp := &s
	fmt.Println(sp.age)

	sp.age = 51
	fmt.Println(sp.age)

	dog := struct {
		name   string
		isGood bool
	}{
		"Rex",
		true,
	}
	fmt.Println(dog)
}
